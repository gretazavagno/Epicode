  var selezione = document.getElementById('selezione');
var scheda = document.getElementById('scheda');
var selezionato;
var btnScegli = document.getElementById('scegli');
var btnDownload = document.getElementById('download');
var progressBar = document.getElementById('progressBar');
var barra;
var larghezza;

window.addEventListener('load', function() {
    document.getElementById('success').style.display = 'none';
    btnDownload.setAttribute('disabled', 'true');
});

// Popolamento della combo
for (let i = 0; i < movies.length; i++) {
	selezione.innerHTML += `<option value="${movies[i].imdbID}">${movies[i].Title}</option>`;
}

selezione.addEventListener('change', function () {
    btnDownload.setAttribute('disabled', 'true');
    document.getElementById('success').style.display = 'none';
	selezionato = selezione.value;
	if (selezionato == '') {
		scheda.innerHTML = '';
	} else {
        btnDownload.removeAttribute('disabled');
    }
});

// Ricerca film selezionato

btnScegli.addEventListener('click', function () {
    setTimeout(visualizza, 1000);
});

const visualizza = () => {
    for (let i = 0; i < movies.length; i++) {
		if (movies[i].imdbID == selezionato) {
			scheda.innerHTML = `<h1>${movies[i].Title}</h1><h3>Anno: ${movies[i].Year}</h3><img src=${movies[i].Poster}>`;
		}
	}
}

btnDownload.addEventListener('click', function() {
    document.getElementById('success').style.display = 'none';
    progressBar.style.display = 'block';
    larghezza = 5;
    barra = setInterval(cresci, 50);
});

function cresci() {
    if (larghezza == 500) {
        clearInterval(barra);
        setTimeout(completa, 500);
    } else {
        larghezza = larghezza + 5;
        progressBar.style.width = larghezza + 'px';
    }
}

function completa() {
    progressBar.style.display = 'none';
    document.getElementById('success').style.display = 'block';
}